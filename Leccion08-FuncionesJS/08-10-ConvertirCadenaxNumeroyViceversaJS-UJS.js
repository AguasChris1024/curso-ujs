//Convertir de cadena a número en JS
let a = '10', b = '20';

//Si son cadenas, se concatenan
console.log(a + b);

//Convertir a números para que se sumen
let suma = parseInt(a) + parseInt(b);
console.log(suma);

//Convertir de número a cadena en JS
let c = 10, d = 20;
console.log(c + d);

//Convertir número a cadena
let concatenacion = a.toString() + b.toString();
console.log(concatenacion);